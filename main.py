def get_random_books_from_file(file_path='books.txt', count=3):
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            lines = [line.strip() for line in f.readlines() if line.strip()]
            if not lines:
                return []
            
            selected = random.sample(lines, min(count, len(lines)))
            formatted = []
            
            for i, entry in enumerate(selected, 1):
                parts = entry.split('|')
                if len(parts) >= 3:
                    title = parts[0].strip()
                    author = parts[1].strip()
                    desc = parts[2].strip()
                    formatted.append(f"{i}️⃣ **«{title}» | {author}**\n_{desc}_")
                else:
                    # Якщо рядок раптом без розділювачів
                    formatted.append(f"{i}️⃣ {entry}")
            return formatted
    except Exception as e:
        print(f"Помилка зчитування файлу: {e}")
        return []

